package Model;

public class CloudObject {
	public int ID;
	public int ID_TOKEN;
	public String TOKEN;
	public String CODE_TOKEN;
	
	public int getID() {
		return ID;
	}
	public void setID(int iD) {
		ID = iD;
	}
	public int getID_TOKEN() {
		return ID_TOKEN;
	}
	public void setID_TOKEN(int iD_TOKEN) {
		ID_TOKEN = iD_TOKEN;
	}
	public String getTOKEN() {
		return TOKEN;
	}
	public void setTOKEN(String tOKEN) {
		TOKEN = tOKEN;
	}
	public String getCODE_TOKEN() {
		return CODE_TOKEN;
	}
	public void setCODE_TOKEN(String cODE_TOKEN) {
		CODE_TOKEN = cODE_TOKEN;
	}
	
}
